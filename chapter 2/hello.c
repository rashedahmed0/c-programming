#include <stdio.h>
#include <math.h>

int main()
{
    //  relation operator

    printf("%d \n", 4 == 4);
    printf("%d \n", 4 == 3);
    printf("%d \n", 4 != 3);
    printf("%d \n", 4 != 4);
    printf("%d \n", 4 > 4);
    printf("%d \n", 4 >= 4);
    printf("%d \n", 4 <= 4);
    printf("%d \n", 4 < 4);

    return 0;
}
